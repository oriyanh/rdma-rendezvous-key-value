#include <infiniband/verbs.h>
#include "common/constants.h"
#include "server.h"
#include "client.h"
#include <sys/param.h>

int main_loop(char *servername, char *input_file) {
    if (servername) {
        client_loop(servername, input_file);
    }
    else {
        server_loop();
    }
    return 0;
}

int main(int argc, char *argv[])
{
    char *servername = (argc > 1) ? strdup(argv[1]) : NULL;
    char *input_file = (argc == 3) ? strdup(argv[2]) : NULL;
    main_loop(servername, input_file);
    return 0;
}
